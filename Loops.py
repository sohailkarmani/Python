# Example 1: for loop
fruits = ["apple", "banana", "cherry"]

# Iterate over each element in the list
for fruit in fruits:
    print(fruit)

# Output:
# apple
# banana
# cherry



#   Example 2: while loop
count = 0

# Loop until count reaches 5
while count < 5:
    print(count)
    count += 1

# Output:
# 0
# 1
# 2
# 3
# 4
