# Variable assignment
name = "John"
age = 25
height = 1.75
is_student = True

# Variable usage
print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Is Student:", is_student)

# Variable reassignment
age = 26
height = 1.80

# Updated variable values
print("Updated Age:", age)
print("Updated Height:", height)

# Variable operations
x = 5
y = 3

sum = x + y
difference = x - y
product = x * y
quotient = x / y

print("Sum:", sum)
print("Difference:", difference)
print("Product:", product)
print("Quotient:", quotient)
