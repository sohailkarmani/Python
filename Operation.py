# Addition
result = 2 + 3
print("Addition:", result)

# Subtraction
result = 5 - 2
print("Subtraction:", result)

# Multiplication
result = 4 * 6
print("Multiplication:", result)

# Division
result = 10 / 2
print("Division:", result)

# Exponentiation
result = 2 ** 3
print("Exponentiation:", result)

# Floor Division
result = 7 // 3
print("Floor Division:", result)

# Modulo
result = 7 % 3
print("Modulo:", result)
